MAT = input ("Escribe el nombre de la materia ")
CAL1 = int (input ("Escribe la primera calificacion ")) 
CAL2 = int (input ("Escribe la segunda calificacion "))
CAL3 = int (input ("Escribe la tercera calificacion "))
CAL4 = int (input ("Escribe la cuarta calificacion "))
CAL5 = int (input ("Escribe la quinta calificacion "))
PRO = (CAL1 + CAL2 + CAL3 + CAL4 + CAL5) / 5
print (f"El promedio que sacaste en la materia de { MAT } es { PRO }") 
